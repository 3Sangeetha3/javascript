// JavaScript variables with var,let, orconst.
var x = 2;
var y = 3;
console.log(x+y)
x=10;
console.log(x+y)

//If you think the value of the variable can change, use let.
let a = 5
let b = 6;
console.log(a+b)
a = 10;
console.log(a+b)

//constant values and cannot be changed.
const p = 4;
const q = 5;
console.log(p+q)
///p=5; //we cannot change the value of const 
console.log(p+q)
